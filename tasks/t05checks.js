// K6 Check Feature Exercise Task Definition

// Task Definition
// The purpose of this exercise is to learn how to use the check feature in k6 to assert specific conditions on HTTP responses. Participants will write a k6 script that performs a basic HTTP request and uses checks to verify aspects of the response, such as status code and response body content.

// Steps
// 1. Script Creation:
// - Write a k6 script that sends an HTTP GET request to a specified URL.
// - Use the check function to verify that the HTTP response status code is 200 (OK).
// - Add another check to verify that the response body contains a specific string or pattern.
// 2. Execution:
// - Execute the k6 script from the command line.
// 3. Verification:
// - Observe the console output to see the results of the checks.
// - Ensure that both checks pass, indicating that the HTTP request was successful and the response met the specified criteria.

