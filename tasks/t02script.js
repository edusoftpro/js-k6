// K6 New Feature Exercise

// Task Definition
// The purpose of this exercise is to introduce the k6 new feature, which allows for the quick creation of k6 script templates. Participants will learn to generate a basic test script using the k6 new command and then execute it.

// Steps
// 1. Generate Script Template: Use the k6 new command to create a new k6 script template named "my_test.js".
// 2. Script Inspection: Review the generated "my_test.js" file to understand the basic structure of a k6 script.
// 3. Script Execution: Execute the "my_test.js" script using the k6 command.
// 4. Output Verification: Check the console output to observe the default actions and metrics being reported by the script.

